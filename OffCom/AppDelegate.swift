extension UIViewController {
  var navBarOffset: CGFloat {
    return sharedApp.statusBarFrame.size.height +
      navigationController!.navigationBar.frame.size.height
  }
}

extension UIFont {
  class func slimWithSize(fontSize: CGFloat) -> UIFont {
    return UIFont (name: "Avenir-Roman", size: fontSize)!
  }
  
  class func boldWithSize(fontSize: CGFloat) -> UIFont {
    return UIFont (name: "Avenir-Black", size: fontSize)!
  }
}

extension UIColor {
  convenience init(hexString: String) {
    let hex = hexString.trimmingCharacters(in: CharacterSet.alphanumerics.inverted)
    var int = UInt32()
    Scanner(string: hex).scanHexInt32(&int)
    let a, r, g, b: UInt32
    switch hex.characters.count {
    case 3: // RGB (12-bit)
      (a, r, g, b) = (255, (int >> 8) * 17, (int >> 4 & 0xF) * 17, (int & 0xF) * 17)
    case 6: // RGB (24-bit)
      (a, r, g, b) = (255, int >> 16, int >> 8 & 0xFF, int & 0xFF)
    case 8: // ARGB (32-bit)
      (a, r, g, b) = (int >> 24, int >> 16 & 0xFF, int >> 8 & 0xFF, int & 0xFF)
    default:
      (a, r, g, b) = (255, 0, 0, 0)
    }
    self.init(red: CGFloat(r) / 255, green: CGFloat(g) / 255, blue: CGFloat(b) / 255, alpha: CGFloat(a) / 255)
  }
}




